class Rectangle:
    def __init__(self, x, y, height, width):
      self.x = x
      self.y = y
      self.height = height
      self.width = width


def __str__(self): 
  '''
  This function creates a f-string of all the inputted parameters
  '''
  return F"x:{self.x} y:{self.y} width:{self.width} height:{self.height}"


